package encapsulation;

public class EncapCar {
	//data members
    private int speed;
    private String color;     

    //getter setters of above data members.
    public int getSpeed() {
          return speed;
    }

    public void setSpeed(int speed) {
          this.speed = speed;
    }

    public String getColor() {
          return color;
    }

    public void setColor(String color) {
          this.color = color;
    }    

}
